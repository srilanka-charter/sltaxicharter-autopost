def get_tweet_for_slot() -> str:
    """Select a tweet based on current date and time slot."""
    now = datetime.utcnow()
    # Determine time slot (0=morning/06JST, 1=noon/12JST, 2=evening/19JST)
    if now.hour == 21 or now.hour == 22:  # 06:00-07:59 JST
        slot = 0
    elif now.hour == 3 or now.hour == 4:   # 12:00-13:59 JST
        slot = 1
    else:                                   # 19:00+ JST (10:00+ UTC)
        slot = 2
    
    # Use date + slot as seed for consistent but varied selection
    day_of_year = now.timetuple().tm_yday
    year = now.year
    seed_str = f"{year}-{day_of_year}-{slot}"
    seed = int(hashlib.md5(seed_str.encode()).hexdigest(), 16)
    index = seed % len(TWEETS)
    return TWEETS[index]

def post_to_x(text: str) -> str:
    client = tweepy.Client(
        consumer_key=API_KEY,
        consumer_secret=API_KEY_SECRET,
        access_token=ACCESS_TOKEN,
        access_token_secret=ACCESS_TOKEN_SECRET
    )
    response = client.create_tweet(text=text)
    return response.data['id']

