def get_unique_tweet() -> str:
    """Generate a unique tweet by combining components with timestamp seed."""
    now_jst = datetime.now(JST)
    
    # Create a seed that changes every run (includes minute for uniqueness)
    seed_str = f"{now_jst.year}-{now_jst.month}-{now_jst.day}-{now_jst.hour}-{now_jst.minute}"
    seed = int(hashlib.sha256(seed_str.encode()).hexdigest(), 16)
    rng = random.Random(seed)
    
    # Pick a theme
    theme = rng.choice(TWEET_COMPONENTS)
    
    # Pick one variation of each component
    opening = rng.choice(theme["openings"])
    body = rng.choice(theme["bodies"])
    
    # Pick 2-3 hashtags
    num_tags = rng.randint(2, 3)
    tags = rng.sample(theme["hashtags"], min(num_tags, len(theme["hashtags"])))
    hashtag_str = " ".join(tags)
    
    tweet = f"{opening}{body} {hashtag_str}"
    
    # Ensure tweet is within 280 characters
    if len(tweet) > 280:
        tweet = tweet[:277] + "..."
    
    return tweet


def post_to_x(text: str) -> str:
    client = tweepy.Client(
        consumer_key=API_KEY,
        consumer_secret=API_KEY_SECRET,
        access_token=ACCESS_TOKEN,
        access_token_secret=ACCESS_TOKEN_SECRET
    )
    response = client.create_tweet(text=text)
    return response.data['id']


